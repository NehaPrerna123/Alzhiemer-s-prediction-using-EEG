# === Training and Evaluation ===
def train(model, train_x, train_y, edge_index, epochs=100):
    optimizer = optim.Adam(model.parameters(), lr=0.005)
    loss_fn = nn.CrossEntropyLoss()
    for epoch in range(epochs):
        model.train()
        optimizer.zero_grad()
        out = model(train_x, edge_index)
        loss = loss_fn(out, train_y)
        loss.backward()
        optimizer.step()
        if (epoch+1) % 10 == 0:
            print(f"Epoch {epoch+1}/{epochs}, Loss: {loss.item():.4f}")

def evaluate(model, test_x, test_y, edge_index):
    model.eval()
    with torch.no_grad():
        out = model(test_x, edge_index)
        preds = out.argmax(dim=1)
        acc = accuracy_score(test_y.cpu(), preds.cpu())
        precision, recall, f1, _ = precision_recall_fscore_support(test_y.cpu(), preds.cpu(), average='macro')
        cm = confusion_matrix(test_y.cpu(), preds.cpu())
        print(f"Accuracy: {acc:.4f}, Precision: {precision:.4f}, Recall: {recall:.4f}, F1-score: {f1:.4f}")
        ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=['Normal', 'MCI', 'AD']).plot(cmap='Blues')
        plt.show()

# === Explainability ===
def explain(model, test_x, edge_index, target_class=2):
    wrapped = WrappedModel(model, edge_index)
    ig = IntegratedGradients(wrapped)
    attributions, delta = ig.attribute(test_x, target=target_class, return_convergence_delta=True)
    plt.figure(figsize=(10, 4))
    plt.plot(attributions[0].cpu().numpy())
    plt.title("Integrated Gradients Attribution")
    plt.xlabel("Input Feature Index")
    plt.ylabel("Attribution Value")
    plt.grid(True)
    plt.tight_layout()
    plt.show()

