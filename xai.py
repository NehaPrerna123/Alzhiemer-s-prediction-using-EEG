class ExplainableHybridGATv2LSTM(nn.Module):
    def __init__(self, input_dim, hidden_dim, lstm_hidden_dim, output_dim, num_heads=4):
        super().__init__()
        self.gatv2 = GATv2Conv(input_dim, hidden_dim, heads=num_heads, concat=True)
        self.norm1 = nn.LayerNorm(hidden_dim * num_heads)
        self.lstm = nn.LSTM(hidden_dim * num_heads, lstm_hidden_dim, num_layers=2, batch_first=True, dropout=0.3)
        self.fusion = nn.Linear((hidden_dim * num_heads) + lstm_hidden_dim, hidden_dim * num_heads)
        self.final_fc = nn.Linear(hidden_dim * num_heads, output_dim)
        self.dropout = nn.Dropout(0.3)

    def forward(self, x, edge_index):
        gat_out = self.gatv2(x, edge_index)
        gat_out = torch.relu(self.norm1(gat_out))
        lstm_out, _ = self.lstm(gat_out.unsqueeze(0))
        lstm_out = lstm_out.squeeze(0)
        fused = torch.cat((gat_out, lstm_out), dim=1)
        fused = self.fusion(fused)
        combined = torch.relu(fused + gat_out)
        combined = self.dropout(combined)
        return self.final_fc(combined)

