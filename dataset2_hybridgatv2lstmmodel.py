def train_model(model, train_features, edge_index, train_labels, test_features=None, test_labels=None, epochs=450):
    optimizer = optim.Adam(model.parameters(), lr=0.005)
    criterion = nn.CrossEntropyLoss()

    train_losses, val_losses = [], []
    train_accuracies, val_accuracies = [], []

    for epoch in range(epochs):
        model.train()
        optimizer.zero_grad()
        out = model(train_features, edge_index)
        loss = criterion(out, train_labels)
        loss.backward()
        optimizer.step()

        # Training metrics
        preds_train = out.argmax(dim=1)
        train_acc = accuracy_score(train_labels.cpu(), preds_train.cpu())
        train_losses.append(loss.item())
        train_accuracies.append(train_acc)

        # Validation metrics
        model.eval()
        with torch.no_grad():
            val_out = model(test_features, edge_index)
            val_loss = criterion(val_out, test_labels)
            preds_val = val_out.argmax(dim=1)
            val_acc = accuracy_score(test_labels.cpu(), preds_val.cpu())
            val_losses.append(val_loss.item())
            val_accuracies.append(val_acc)

        if (epoch + 1) % 20 == 0:
            print(f"Epoch {epoch+1}/{epochs} | Train Loss: {loss.item():.4f} | Val Loss: {val_loss.item():.4f} | Train Acc: {train_acc:.4f} | Val Acc: {val_acc:.4f}")

    # Final evaluation and plot
    evaluate_model(model, test_features, edge_index, test_labels)
    plot_metrics(train_losses, val_losses, train_accuracies, val_accuracies, model.__class__.__name__)


def evaluate_model(model, test_features, edge_index, test_labels):
    model.eval()
    with torch.no_grad():
        out = model(test_features, edge_index)
        preds = out.argmax(dim=1).cpu().numpy()
        true = test_labels.cpu().numpy()
        acc = accuracy_score(true, preds)
        precision, recall, f1, _ = precision_recall_fscore_support(true, preds, average='weighted', zero_division=0)
        print(f"Accuracy:  {acc*100:.2f}%")
        print(f"Precision: {precision:.4f}, Recall: {recall:.4f}, F1-Score: {f1:.4f}")

        cm = confusion_matrix(true, preds)
        disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=['HC', 'MCI', 'AD'])
        disp.plot(cmap='Blues')
        plt.title("Confusion Matrix")
        plt.show()

        return acc, precision, recall, f1

